package com.example.android.mybook;

public class Users {
    String name1;
    String email1;

    public Users(String name, String email) {
        this.name1 = name;
        this.email1 = email;

    }

    public Users() {
    }

    public String getName() {
        return name1;
    }

    public String getEmail() {
        return email1;
    }
}
